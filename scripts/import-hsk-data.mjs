const sentenceTemplates = [
  ["我学习{hanzi}。", "Wǒ xuéxí {pinyin}.", "Tôi học từ \"{vi}\"."],
  ["这个词是{hanzi}。", "Zhège cí shì {pinyin}.", "Từ này là \"{vi}\"."],
  ["请你读{hanzi}。", "Qǐng nǐ dú {pinyin}.", "Hãy đọc từ \"{vi}\"."],
  ["我今天复习{hanzi}。", "Wǒ jīntiān fùxí {pinyin}.", "Hôm nay tôi ôn từ \"{vi}\"."]
];

function toId(text) {
  return text
    .normalize("NFKD")
    .replace(/[^\p{Letter}\p{Number}]+/gu, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
}

function cleanEnglish(translations) {
  return translations
    .slice(0, 2)
    .map((item) => item.replace(/CL:.*/g, "").replace(/\[[^\]]+\]/g, "").trim())
    .filter(Boolean)
    .join(" / ");
}

function wordToItem(level, raw) {
  const vi = viOverrides[raw.hanzi] ?? `EN: ${cleanEnglish(raw.translations)}`;
  return {
    id: `${level.toLowerCase()}-${raw.id}-${toId(raw.hanzi)}`,
    level,
    hanzi: raw.hanzi,
    pinyin: raw.pinyin,
    vietnamese: vi,
    example: `我学习${raw.hanzi}。`,
    examplePinyin: `Wǒ xuéxí ${raw.pinyin}.`,
    exampleVietnamese: `Tôi học từ "${vi}".`
  };
}

const allWords = [];
const allSentences = [];

for (const [level, url] of sources) {
  const response = await fetch(url);
  if (!response.ok) throw new Error(`Failed to fetch ${url}`);
  const rawWords = await response.json();
  const levelWords = rawWords.map((word) => wordToItem(level, word));
  allWords.push(...levelWords);

  levelWords.slice(0, 60).forEach((word, index) => {
    const template = sentenceTemplates[index % sentenceTemplates.length];
    allSentences.push({
      id: `s-${word.id}`,
      level,
      chinese: template[0].replace("{hanzi}", word.hanzi),
      pinyin: template[1].replace("{pinyin}", word.pinyin),
      vietnamese: template[2].replace("{vi}", word.vietnamese),
      keywords: [word.hanzi]
    });
  });
}

const output = `import type { SentenceItem, WordItem } from "../types";

// Generated from clem109/hsk-vocabulary MIT-licensed HSK JSON data.
// Vietnamese translations are local overrides where available; remaining entries keep English fallback prefixed with EN.
const wordsJson = ${JSON.stringify(JSON.stringify(allWords))};

const sentencesJson = ${JSON.stringify(JSON.stringify(allSentences))};

export const generatedWords = JSON.parse(wordsJson) as WordItem[];

export const generatedSentences = JSON.parse(sentencesJson) as SentenceItem[];
`;

writeFileSync("src/data/generated-hsk.ts", output);
